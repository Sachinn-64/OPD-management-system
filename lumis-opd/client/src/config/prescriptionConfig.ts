// Helper function to get translated value
export const getTranslation = (
  translations: { en: string; hi: string; mr: string; kn: string },
  language: Language
): string => {
  return translations[language] || translations.en;
};

// Helper function to detect form from medicine name
export const detectFormFromName = (medicineName: string): MedicineForm => {
  const name = medicineName.toLowerCase();

  if (/\b(tab|tablet)\b/i.test(name)) return 'TAB';
  if (/\b(cap|capsule)\b/i.test(name)) return 'CAP';
  if (/\b(syp|syrup|susp|suspension|liquid|liq|elixir)\b/i.test(name)) return 'SYP';
  if (/\b(inj|injection|iv|vial|ampoule)\b/i.test(name)) return 'INJ';
  if (/\b(cream|ointment)\b/i.test(name)) return 'CREAM';
  if (/\b(gel)\b/i.test(name)) return 'GEL';
  if (/\b(drop|drops)\b/i.test(name)) return 'DROPS';
  if (/\b(powder|sachet|granules)\b/i.test(name)) return 'POWDER';

  return 'OTHER';
};
